package homework7;

public class Calculator {

    public double jia(double m, double n){
        return m + n;
    }

    public double jian(double m, double n) {
        return m - n;
    }

    public double cheng(double m, double n) {
        return m * n;
    }

    public double chu(double m, double n) {
        return m / n;
    }
}
