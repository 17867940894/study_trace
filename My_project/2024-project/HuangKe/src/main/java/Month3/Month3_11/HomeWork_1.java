package Month3.Month3_11;

import java.util.Scanner;

public class HomeWork_1 {
    /*
     1.将给定小写字母转换成大写字母
     */
    public static void main(String[] args) {
        char lowercaseLetter = 'a'; // 要转换的小写字母
        char uppercaseLetter = Character.toUpperCase(lowercaseLetter); // 将小写字母转换为大写字母
        System.out.println("大写字母: " + uppercaseLetter);
    }
}
